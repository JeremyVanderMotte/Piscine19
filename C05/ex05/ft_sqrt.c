int		ft_sqrt(int nb)
{
	int		res;

	res = 2;
	if (nb < 0)
		return (0);
	if (nb == 1)
		return (1);
	while (res <= nb / res)
	{
		if (res * res == nb)
			return (res);
		res++;
	}
	return (0);
}
