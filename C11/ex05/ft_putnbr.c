#include "ft.h"

void	ft_putnbr(int nbr)
{
	unsigned int	nb;

	nb = nbr;
	if (nbr < 0)
	{
		ft_putchar('-');
		nb = -nbr;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
}
