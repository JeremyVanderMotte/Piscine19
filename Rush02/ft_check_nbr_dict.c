#include "ft.h"

int		ft_check_nbr_dict(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(ft_is_nbr(*(str + i))))
			return (0);
		i++;
	}
	return (1);
}
