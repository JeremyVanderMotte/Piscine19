char	*ft_strncpy(char *dest, char *src, int n)
{
	int 	cpt;

	cpt = 0;
	while (*(src + cpt) != '\0' && cpt < n)
	{
		*(dest + cpt) = *(src + cpt);
		cpt++;
	}
	while (cpt < n)
	{
		*(dest + cpt) = '\0';
		cpt++;
	}
	return (dest);
}
