int		ft_str_is_numeric(char *str)
{
	int		cpt;
	char	temp;

	cpt = 0;
	while (*(str + cpt) != '\0')
	{
		temp = *(str + cpt);
		if (!(temp <= '9' && temp >= '0'))
			return (0);
		cpt++;
	}
	return (1);
}
