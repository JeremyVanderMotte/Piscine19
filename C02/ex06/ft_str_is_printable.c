int		ft_str_is_printable(char *str)
{
	int		cpt;
	char	temp;

	cpt = 0;
	while (*(str + cpt) != '\0')
	{
		temp = *(str + cpt);
		if (!(temp <= 126 && temp >= 32))
			return (0);
		cpt++;
	}
	return (1);
}
