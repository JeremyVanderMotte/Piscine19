char	*ft_strlowcase(char *str)
{
	int		cpt;
	char	temp;

	cpt = 0;
	while (*(str + cpt) != '\0')
	{
		temp = *(str + cpt);
		if (temp >= 'A' && temp <= 'Z')
			*(str + cpt) = temp + 32;
		cpt++;
	}
	return (str);
}
