void	ft_rev_int_tab(int *tb, int size)
{
	int temp;
	int cpt;

	cpt = 0;
	while (cpt < (size / 2))
	{
		temp = *(tb + cpt);
		*(tb + cpt) = *(tb + (size - cpt - 1));
		*(tb + (size - cpt - 1)) = temp;
		cpt++;
	}
}
